/**
 * Cosine similarity for Brand Brain re-ranking (Faz 1). Pure + dependency-free.
 * Returns a value in [-1, 1]; 0 when either vector is empty or zero-length (so an
 * un-embedded chunk simply ranks last rather than throwing).
 */
export function cosineSimilarity(a: number[], b: number[]): number {
  if (!a?.length || !b?.length || a.length !== b.length) return 0;
  let dot = 0;
  let na = 0;
  let nb = 0;
  for (let i = 0; i < a.length; i++) {
    dot += a[i] * b[i];
    na += a[i] * a[i];
    nb += b[i] * b[i];
  }
  if (na === 0 || nb === 0) return 0;
  return dot / (Math.sqrt(na) * Math.sqrt(nb));
}
